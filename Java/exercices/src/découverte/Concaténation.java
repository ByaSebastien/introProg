package découverte;

public class Concaténation {

	public static void main(String[] args) {
		int a=3,b=5; 
		System.out.println(a + "+" + b +"=" + a+b); // 3+5=35
		// rajouter les () pour différencier la concaténation à l'addition (ordre de prioritée)
		System.out.println(a + "+" + b +"=" + (a+b)); // 3+5=8
		

	}

}
